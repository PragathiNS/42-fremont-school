#include "libft.h"

int		ft_intlen(int nbr)
{
	int	i;

	i = 0;
	if (nbr < 0)
	{
		nbr = nbr * (-1);
		i++;
	}
	while (nbr >= 10)
	{
		i++;
		nbr /= 10;
	}
	if ((nbr <= 9) && (nbr >= 0))
		i++;
	return (i);
}
