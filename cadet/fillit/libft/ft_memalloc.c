#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	if (!(mem = (void *)malloc(size * (sizeof(mem)))))
		return (NULL);
	else
		ft_memset(mem, 0, size);
	return (mem);
}
