#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t		i;
	int			nbr;
	int			neg;

	i = 0;
	nbr = 0;
	neg = 1;
	while (ft_whitespace((int)str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	return (nbr * neg);
}
